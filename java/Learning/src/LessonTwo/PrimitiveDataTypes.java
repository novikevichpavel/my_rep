package LessonTwo;

public class PrimitiveDataTypes {
    public static void main(String[] args) {

        byte bOne = 127;
        byte bTwo = -128;
        // 8 бит

        short shOne = 32767;
        short shTwo = -32768;
        //16 бит

        int iOne = 2147483647;
        int iTwo = -2147483648;
        // 32 бит

        long lgOne = 10000000000L;

        float f1 = 1.5F;
        double d1 = 1.546D;

        char charOne = '\u05AB';

        System.out.println(charOne);

        int a7 = 1_000_000_000;

        System.out.println(a7);
    }
}
